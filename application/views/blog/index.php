		<p>Bootstrap 3.3.0 is here! This release has been all about bug fixes, accessibility improvements, and documentation updates. We’ve had over 700 commits from 28 contributors since our last release. Woohoo!</p>

		<p>Here are some of the highlights:</p>

		<ul>
		  <li>Added a handful of new Less variables for easier customization.</li>
		  <li>Removed recent progress bar changes for low percentages.</li>
		  <li>Removed all instances of <code>translate3d</code> as they improved repaint performance, but also added several cross browser bugs.</li>
		  <li>Added <code>transform</code>s to improve carousel performance in modern browsers.</li>
		  <li>Updated Normalize.css and our H5BP print styles to their latest releases.</li>
		  <li>Improved accessibility for navs, panels, tooltips, buttons, and more.</li>
		  <li>Resolved dozens of JavaScript and documentation bugs.</li>
		</ul>

		<p>For a complete breakdown, <a href="https://github.com/twbs/bootstrap/releases/tag/v3.3.0">read the release changelog</a> or the <a href="https://github.com/twbs/bootstrap/issues?q=milestone%3Av3.3.0+is%3Aclosed">v3.3.0 milestone</a>.</p>

		<h2 id="download-bootstrap">Download Bootstrap</h2>

		<p>Download the latest release—source code, compiled assets, and documentation—as a zip file directly from GitHub:</p>

		<p><a class="btn-link" href="https://github.com/twbs/bootstrap/archive/v3.3.0.zip">Download Bootstrap 3.3.0</a></p>

		<p>Hit the <a href="https://github.com/twbs/bootstrap">project repository</a> or <a href="https://github.com/twbs/bootstrap-sass">Sass repository</a> for more options. Also, remember <a href="https://www.npmjs.org/package/bootstrap">we’re available on npm</a>, too.</p>

		<p><em>An update for the Bootstrap CDN will be available shortly.</em></p>

		<h2 id="new-tools">New tools</h2>

		<p>Since our last release, we’ve open sourced two new tools:</p>

		<ul>
		  <li><a href="/2014/09/23/bootlint/">Bootlint</a>, a custom linter for all your Bootstrap projects.</li>
		  <li><a href="/2014/10/13/rorschach/">Rorschach</a>, a bot for checking new pull requests for common mistakes.</li>
		</ul>

		<p>They join <a href="/2014/06/25/lmvtfy/">LMVTFY</a>, our bot for quickly validating HTML in live examples. As the project, team, and community continue to grow, look for even more awesome tools to be open sourced.</p>

		<h2 id="onward-to-bootstrap-4">Onward to Bootstrap 4</h2>

		<p>Perhaps the best part of releasing v3.3.0 today is that we can start to tell you more about Bootstrap 4! While the first alpha is a couple weeks off, here’s a quick preview of what’s to come:</p>

		<ul>
		  <li>Updated grid system with at least one additional tier for handheld devices.</li>
		  <li>A brand new component to replace panels, thumbnails, and wells.</li>
		  <li>A completely new, simpler navbar.</li>
		  <li>Switch all pixel values over to rems and ems for easier and better type and component sizing.</li>
		  <li>Dropped support for IE8.</li>
		  <li>Tons of form updates, including custom form controls.</li>
		  <li>New component animations and transitions for several components.</li>
		  <li>UMD support throughout our JavaScript plugins.</li>
		  <li>Improved JavaScript positioning for tooltips, popovers, and dropdowns.</li>
		  <li>Brand new documentation (written in Markdown, too!).</li>
		  <li>A new approach to configuring global theming options.</li>
		  <li>And hundreds more changes across the board.</li>
		</ul>

		<p>We’d love to tell you more, but the dust still has to settle before we open our first pull request with a live alpha release. In addition to launching in v4 in the coming months, we’ll be maintaining v3 with small bugfixes for the first few months after the new version ships.</p>

